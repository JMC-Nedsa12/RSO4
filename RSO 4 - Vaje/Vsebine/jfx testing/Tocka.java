
public class Tocka
{
    double x,y;
    public Tocka()
    {
        y=0;
        x = 0;
    }
    public Tocka (int x, int y){
        this.x=x;
        this.y=y;
    }
    public double getY(){return this.y;}
    public double getX(){return this.x;}
    public void setY(double y){this.y=y;}
    public void setX(double x){this.x=x;}
}
